package com.java.demo.intro;

public class IncDecDemo {

	public static void main(String[] args) {
		int a = 5;
		 System.out.println(++a);          // a becomes 6
		 System.out.println(a);
		 a++;          // a becomes 7
		 System.out.println(a);
		 --a;          // a becomes 6
		 System.out.println(a);
		 System.out.println(a--);
		 a--;

	}

}
